# 현재 위치 경로를 출력
pwd

# 현재위치에서 파일 및 폴더 조회
ls

# 목록조회 자세히
ls -l

# 목록조회 자세히, 숨김파일까지
ls -al

# 목록조회 자세히, 숨김파일, 오래된 순 정렬(최신은 alt)
ls -alrt (reverse time)

# 디렉토리 생성
mkdir jihyeon_dir(디렉토리명)

# 특정 디렉토리로 이동
cd jihyeon_dir

# 상위 디렉토리로 이동
cd .. 또는 cd ../

# 루트 디렉토리(최상위 디렉토리)로 이동
cd /

# 직전 폴더로 이동
cd -

# 홈경로(/home/본인계정)로 이동
cd

# 상대경로와 절대경로
# 상대경로 : cd ../../
# 절대경로 : cd /home